print(type(15)) # int -> integer -> número entero
print(type(15.0)) # float -> número de punto flotante -> número real
print(type(15j)) # complex -> número complejo -> parte imaginaria 
print(type("15")) # str -> string -> texto
print(type([15, 267, 34])) # list -> lista -> conjunto ordenado y mutable de datos
print(type((15,))) # tuple -> tupla -> conjunto ordenado e inmutable de datos
print(type({15: 15})) # dict -> diccionario -> conjunto nombrado de datos
print(type(True)) #bool -> booleano -> True y False